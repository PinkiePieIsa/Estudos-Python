num = input('Digite algo: ')
print(num.isnumeric())
# Responde se o que foi digitado pode ser convertido em um número.
print(num.isalpha())
# Responde se o que foi digitado é uma letra
print(num.isalnum())
# Responde se é alfanumérico, aceita letras e números
print(num.isupper())
# Responde se é tudo MAIÚSCULO
print(num.islower())
# Responde se é minúsculo
print(num.isprintable())

